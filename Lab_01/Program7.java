package Lab_01;
public class Program7 {
    public static void main(String[] args) {
        int a = 5;
        int b = 4;
        System.out.println("&(AND):");
        System.out.print(a&b);
        System.out.print("|(or):");
        System.out.println(a|b);
        System.out.print("^(XOR):");
        System.out.println(a^b);
        System.out.print("~:");
        System.out.println(~a);
        System.out.print(~b);
    }
    
}
